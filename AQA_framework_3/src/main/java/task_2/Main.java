public class Main {
    public static void main(String[] args) {

        int v1 = 10;
        int v2 = 20;

        if (v1 > v2) {
            System.out.println("Variable 'v1' is greater than 'v2'");
        } else if (v1 < v2) {
            System.out.println("Variable 'v2' is greater than 'v1'");
        } else {
            System.out.println("Both variables are equal");
        }

    }
}

